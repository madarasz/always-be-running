#!/bin/bash
# export-test-db.sh - Export lightweight test database for E2E testing
# Usage: ./scripts/export-test-db.sh [output_file]
#
# Runs mysqldump via Docker container against the abr-mysql service

set -e

OUTPUT_FILE="${1:-e2e/fixtures/test-seed.sql}"
DB_HOST="mysql"
DB_USER="root"
DB_PASS="rootsecret"
DB_NAME="netrunner"

# Test user IDs
TEST_USER_IDS="1276,21903"

# Days for recency filter
RECENCY_DAYS=120

echo "Exporting test database to $OUTPUT_FILE..."

# Calculate cutoff date (120 days ago) - works on both macOS and Linux
if date -v-1d > /dev/null 2>&1; then
  # macOS
  CUTOFF_DATE=$(date -v-${RECENCY_DAYS}d +%Y-%m-%d)
else
  # Linux
  CUTOFF_DATE=$(date -d "${RECENCY_DAYS} days ago" +%Y-%m-%d)
fi
echo "Cutoff date for recent data: $CUTOFF_DATE"

# Create output directory if needed
mkdir -p "$(dirname "$OUTPUT_FILE")"

# Helper function to run mysqldump via Docker
mysqldump_docker() {
    docker compose exec -T mysql mysqldump -h "$DB_HOST" -u "$DB_USER" -p"$DB_PASS" "$@" 2>/dev/null
}

# Start with header
cat > "$OUTPUT_FILE" << 'EOF'
-- AlwaysBeRunning Test Database Seed
-- Auto-generated by scripts/export-test-db.sh - do not edit manually
-- Contains filtered production data for E2E testing

SET SQL_MODE = "NO_AUTO_VALUE_ON_ZERO";
SET FOREIGN_KEY_CHECKS = 0;
SET AUTOCOMMIT = 0;
START TRANSACTION;

EOF

# ====================
# TABLES TO INCLUDE IN FULL (reference data)
# ====================
FULL_TABLES=(
    "badges"
    "countries"
    "tournament_types"
    "tournament_formats"
    "card_cycles"
    "card_packs"
    "card_identities"
    "mwls"
    "artists"
    "prizes"
    "prize_elements"
    "tournament_groups"
    "video_tags"
)

echo "Exporting full tables..."
for table in "${FULL_TABLES[@]}"; do
    echo "  - $table"
    mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
        "$DB_NAME" "$table" >> "$OUTPUT_FILE" || echo "    (table may be empty or not exist)"
done

# ====================
# USERS TABLE - Export test users + users referenced by tournaments and entries
# ====================
echo "Finding users referenced by exported data..."
# Get user IDs from tournaments (creator) and entries (user) that match our export criteria
REFERENCED_USERS=$(docker compose exec -T mysql mysql -N -h "$DB_HOST" -u "$DB_USER" -p"$DB_PASS" "$DB_NAME" -e "
    SELECT DISTINCT id FROM (
        -- Test users
        SELECT id FROM users WHERE id IN ($TEST_USER_IDS)
        UNION
        -- Tournament creators (recent tournaments)
        SELECT DISTINCT creator AS id FROM tournaments WHERE updated_at >= '$CUTOFF_DATE' AND creator IS NOT NULL
        UNION
        -- Entry users (test users + recent entries)
        SELECT DISTINCT user AS id FROM entries WHERE (user IN ($TEST_USER_IDS) OR updated_at >= '$CUTOFF_DATE') AND user IS NOT NULL
    ) AS all_users;
" 2>/dev/null | tr '\n' ',' | sed 's/,$//')

echo "Exporting users (referenced by tournaments/entries)..."
echo "  User IDs: $REFERENCED_USERS"
if [ -n "$REFERENCED_USERS" ]; then
    mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
        --where="id IN ($REFERENCED_USERS)" \
        "$DB_NAME" users >> "$OUTPUT_FILE"
else
    echo "  (no users found)"
fi

# ====================
# BADGE_USER - Only test users
# ====================
echo "Exporting badge_user (test users only)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="user_id IN ($TEST_USER_IDS)" \
    "$DB_NAME" badge_user >> "$OUTPUT_FILE"

# ====================
# PHOTOS - Only test users
# ====================
echo "Exporting photos (test users only)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="user_id IN ($TEST_USER_IDS)" \
    "$DB_NAME" photos >> "$OUTPUT_FILE"

# ====================
# PRIZE_COLLECTIONS - Only test users
# ====================
echo "Exporting prize_collections (test users only)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="user_id IN ($TEST_USER_IDS)" \
    "$DB_NAME" prize_collections >> "$OUTPUT_FILE"

# ====================
# TOURNAMENTS - Only recent (last 120 days)
# ====================
echo "Exporting tournaments (recent only)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="updated_at >= '$CUTOFF_DATE'" \
    "$DB_NAME" tournaments >> "$OUTPUT_FILE"

# ====================
# ENTRIES - Test users OR recent
# Note: `user` column (not `user_id`) holds the user foreign key
# ====================
echo "Exporting entries (test users + recent)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="user IN ($TEST_USER_IDS) OR updated_at >= '$CUTOFF_DATE'" \
    "$DB_NAME" entries >> "$OUTPUT_FILE"

# ====================
# VIDEOS - recent only (videos table has created_at)
# ====================
echo "Exporting videos (recent only)..."
mysqldump_docker --no-create-info --single-transaction --skip-lock-tables \
    --where="created_at >= '$CUTOFF_DATE'" \
    "$DB_NAME" videos >> "$OUTPUT_FILE"

# ====================
# SKIP THESE TABLES
# ====================
# - sessions (runtime data, not needed)
# - migrations (Laravel internal, auto-created)

# Footer
cat >> "$OUTPUT_FILE" << 'EOF'

SET FOREIGN_KEY_CHECKS = 1;
COMMIT;
EOF

# Show file size
FILE_SIZE=$(ls -lh "$OUTPUT_FILE" | awk '{print $5}')
LINE_COUNT=$(wc -l < "$OUTPUT_FILE" | tr -d ' ')

echo ""
echo "Export complete: $OUTPUT_FILE"
echo "  Size: $FILE_SIZE"
echo "  Lines: $LINE_COUNT"
